// Multiplicar dos numeros enteros
#include <iostream>

using namespace std;

int main() {
    // Declarar variables de tipo entero
    int a;
    int b;
    int c;

    cin >> a >> b; // Leer a y b

    cout << a << "\n" << b << "\n"; // Imprimir a y b separados por salto de linea

    c = a * b; // Multiplicacion
    cout << c; // Imprimir el valor de c

    return 0;
}