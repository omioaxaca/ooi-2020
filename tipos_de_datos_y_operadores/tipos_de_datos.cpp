#include <iostream>

using namespace std;

int main() {
    // Variable para almacenar datos enteros, por ejemplo 1, 3, 10, -5
    int numero;
    // Variable para almacenar datos con punto decimal, por ejemplo 1.2, 5.7, 9.34, 3.1416
    double numero_decimal;
    // Variable para almacenar letras y simbolos
    char letra;

    // Lectua de datos
    cin >> numero;
    cin >> numero_decimal;
    cin >> letra;

    // Imprimir las variables
    cout << numero << "\n";
    cout << numero_decimal << "\n";
    cout << letra << "\n";



    return 0;
}