#include <iostream>

using namespace std;

int main() {

    int a;
    int b;
    a = 5;      // Asignacion
    b = 3;      // Asignacion
    a = b + 1;  // Suma
    a = b - 1;  // Resta
    a = a + 1;  // Incremento
    a++;        // Incremento en 1 (es decir, le suma uno a la variable)
    ++a;        // Incremento en 1
    a--;        // Decremento en 1 (es decir, le resta uno a la variable)
    --a;        // Decremento en 1
    a = a + 2;  // Sumar 2 a la variable a
    a += 2;     // Sumar 2 a la variable a


    


    return 0;
}